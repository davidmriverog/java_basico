/***
 * - Estructura repetitiva while
 * 
 */
package eje_curso_03;

public class Eje_curso_03 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int x = 0;
        
        while(x<10){
            
            System.out.println("Mensaje:"+ (x+1)+" Uso del While");
            
            x++; // Incrementador
        }
    }
    
}
